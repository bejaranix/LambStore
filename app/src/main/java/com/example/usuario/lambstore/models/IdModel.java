/**
 * Defines a model object that has a id field.*
 */

public interface IdModel {

    /**
     * Gets the id of the model
     * @return {@link Long}, the id.
     */
    Long getId();

    /**
     * Sets the id of the model
     * @param {@link Long} id, the id.
     */
    void setId(Long id);
}
